public class Reso {
    private String id;
    private String name;
    private String link;
    private String des;

    public void setid(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setname(String name) {
        this.name = name;
    }

    public String getname() {
        return name;
    }

    public void setlink(String link) {
        this.link = link;
    }

    public String getlink() {
        return link;
    }

    public void setdes(String des) {
        this.des = des;
    }

    public String getdes() {
        return des;
    }

}
